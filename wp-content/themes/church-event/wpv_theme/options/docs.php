will need "Link" post format if you want clicking on the image to lead
to the portfolio post.
<br><br><b>Gallery</b> - Use the "Add Media" button in a text/image
block element to create a gallery.This button is also found in the top
left side of the visual and text editors. Please note that when the
media manager opens up in the lightbox, you have to click on "Create
Gallery" on the left and then select the images for your gallery.
<br><br><b>Video - </b>Put the url of the video in the post format
option field above the editor. You must use an oEmbed provider supported
by WordPress or a file supported by the [video] shortcode which comes
with WordPress. Vimeo and Youtube are supported.<br><br><b>Link</b> -
Use the standard Featured Image option for the portfolio image. Use the
editor for your content. Put the link in the post format option field
below if you want the image in the portfolio listing to lead to a
particular link. If you leave the link field blank, clicking on the
image in the portfolio listing page will open up the portfolio post.
<br><br><b>Document</b> - Use the standard Featured Image option for the
portfolio image. Use the editor for your content. The image will only
be shown in the portfolio listing.You will need "Link" post format if
you need the featured image to appear in the post itself<br><br><b>HTML</b> - Use the standard Featured Image option for the portfolio image. Use the editor for your content. <br><br><p><br></p>
