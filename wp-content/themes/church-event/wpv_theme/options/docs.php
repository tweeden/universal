<p><b>Why not a Support Forum?</b></p>
<p>Simply put, so we can offer you superior support.</p>
<p>A forum works only when you have a large enough active community -
thousands of people willing to participate on a daily basis. The smaller
the community, the more likely you are to wait for a long time before
someone answers your question. Unfortunately even the top authors in
Themeforest have only a few hundreds active users on a daily basis and
this is not enough to make the forum works</p>
